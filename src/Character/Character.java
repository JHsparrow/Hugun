package Character;

public class Character {
	private int Hp = 200;
	private int AttactDamage = 10;
	private int AttactSpeed;
	
	public int getHp() {
		return Hp;
	}	
	public void setHp(int hp) {
		Hp = hp;
	}
	public int getAttactDamage() {
		return AttactDamage;
	}
	public void setAttactDamage(int attactDamage) {
		AttactDamage = attactDamage;
	}
	public int getAttactSpeed() {
		return AttactSpeed;
	}
	public void setAttactSpeed(int attactSpeed) {
		AttactSpeed = attactSpeed;
	}
	
	
	
}
